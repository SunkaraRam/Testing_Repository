package lakshmi1;

public class SwitchDemo {

	public static void main(String[] args) {
	    
		int age=7;
		switch (age) {
		case 1:
		System.out.println("ramani");
		break;
		case 2:
		System.out.println("lakshmi");
		break;
		case 3:
		System.out.println("ramu");
		case 4:
		System.out.println("sreenath");
		break;
		case 5:
		System.out.println("ravi");
		break;
		case 6:
		System.out.println("mallama");
		break;
		default:
		System.out.println("kaki");
		
		}
		
	}

}
