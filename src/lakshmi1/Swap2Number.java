public class Swap2Number {

	public static void main(String[] args) {

		int num1=100;
		int num2=50; 
		//logic

		num1 =num1+num2;//num1=100+50=150;
		num2 =num1-num2;//num2=150-50=100;
		num1 =num1-num2;//num2=150-100=50;
		
		System.out.println("after swapping of 2 members");
		
		System.out.println("num one value is" +num1);
		System.out.println("num one value is" +num2);
		

		
	}

}
